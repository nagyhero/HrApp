package com.SmartTech.hrapp.Model;

public class PositionsModel {
    private String id,name,employees,due_day,avg_salary;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmployees() {
        return employees;
    }

    public void setEmployees(String employees) {
        this.employees = employees;
    }

    public String getDue_day() {
        return due_day;
    }

    public void setDue_day(String due_day) {
        this.due_day = due_day;
    }

    public String getAvg_salary() {
        return avg_salary;
    }

    public void setAvg_salary(String avg_salary) {
        this.avg_salary = avg_salary;
    }
}
